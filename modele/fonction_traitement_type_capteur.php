<?php
function type_capteur($numeroserie)
{
  $type1=substr($numeroserie,0,1);
  if($type1=="T")
  {
    $type2="Temperature";
  }
  elseif($type1=="I")
  {
    $type2="Presence";
  }
  elseif($type1=="F")
  {
    $type2="Fumee";
  }  
  elseif($type1=="L")
  {
    $type2="Luminosite";
  }
  elseif($type1=="W")
  {
    $type2="Volet";
  }
  elseif($type1=="P")
  {
    $type2="Prise";
  }
  elseif($type1=="A")
  {
    $type2="Alarme";
  }
  return $type2;
}
?>