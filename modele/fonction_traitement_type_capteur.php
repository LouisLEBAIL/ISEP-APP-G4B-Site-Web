<?php
function type_capteur($numeroserie)
{
  $type1=substr($numeroserie,0,2);
  if($type1=='TA')
  {
    $type2='Mode_temperature';
  }
  elseif($type1=="T0")
  {
    $type2="Temperature";
  }
  elseif($type1=="I0")
  {
    $type2="Presence";
  }
  elseif($type1=="F0")
  {
    $type2="Fumee";
  }  
  elseif($type1=="L0")
  {
    $type2="Luminosite";
  }
  elseif($type1=="W0")
  {
    $type2="Volet";
  }
  elseif($type1=="P0")
  {
    $type2="Prise";
  }
  elseif($type1=="A0")
  {
    $type2="Alarme";
  }
  return $type2;
}
?>